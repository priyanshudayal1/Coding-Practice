public class lec05 {
    public static void main(String[] args) {
        int p=20;
        int q=10;
        System.out.println(p+q);
        System.out.println(p-q);
        System.out.println(p*q);
        System.out.println(p/q);
        System.out.println(p%q);
        System.out.println(p++);
        System.out.println(p--);
        System.out.println(p>q);
        System.out.println("Program completed");
        
    }
}
